from pylab import *

# load data from my calculations
pyshape_folder = './pyshape_results/'
Omega, Pop_T4_P = loadtxt(pyshape_folder + 'T_4K_P.txt', delimiter=' ', unpack=True, skiprows=1)
Omega, Pop_T4_N = loadtxt(pyshape_folder + 'T_4K_N.txt', delimiter=' ', unpack=True, skiprows=1)
Omega, Pop_T12_P = loadtxt(pyshape_folder + 'T_12K_P.txt', delimiter=' ', unpack=True, skiprows=1)
